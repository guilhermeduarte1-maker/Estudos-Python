# O QUE FAZER:
# Crie uma contagem regressiva que mostre os números de 10 até 1.
# No final da contagem, exiba a mensagem: "Fogo!"
# ==========================================

# Escreva seu código aqui:
i = 10
while i >= 1:
    print(i)
    i -= 1

print("Fogo!")